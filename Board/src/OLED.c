void OLED_Show_Cross(uint8 *table, uint8 wide, uint8 high)  //显示横向图片x*y,直接显示压缩图片
{
    uint8 i, x, y;
    uint8 temp = 0;
    uint8 store = 0;

    uint8 line_num;         //八行组成一行，行数
    uint8 leap_line;        //一行偏移字节数
    uint8 leap_group;       //八行偏移字节数
    uint16 max_offset;      //最大偏移地址,注意范围
    uint16 current_offset;   //当前相对偏移地址

    line_num = (high >> 3) + (high % 8 || 0);       //行数不满足八的倍数是增加一行补齐为八的倍数
    leap_line = (wide >> 3) + (wide % 8 || 0);      //宽度不满足八的倍数时增加一列补齐为八的倍数
    leap_group = leap_line << 3;
    max_offset = leap_line * high;

    for(y = 0; y < line_num; y ++)
	{
		OLED_Set_Pos(0, y);
		for(x = 0; x < wide; x ++)
        {
            for(i = 0; i < 8; i ++)
            {
                temp = temp >> 1;
                current_offset = i * leap_line + y * leap_group + (x >> 3);     //计算出当前相对偏移地址
                if(current_offset >= max_offset)
                    store = 0xff;                                               //超出部分显示黑色
                else
                    store = *(table + current_offset);
                temp |= (store << x % 8) & 0x80;
            }
            OLED_WrDat(~temp);
            temp = 0;
        }
	}
}

void OLED_Show_Uncompress(uint8 *Table,uint16 usRowNum, uint16 usColumnNum)
{
    uint8 ucData;
    uint16 i,j,k,m,n;
    uint16 usRowTmp;

    m = usRowNum >> 3;                          //计算图片行数以8位为一组完整的组数
    n = usRowNum % 8;                           //计算分完组后剩下的行数

    for(i = 0; i < m; i++)                      //完整组行扫描
    {
        OLED_Set_Pos(0,(uint8)i);
        usRowTmp = i << 3;                      //计算当前所在行的下标
        for(j = 0; j < usColumnNum; j++)        //列扫描
        {
            ucData = 0;
            for(k = 0; k < 8; k++)              //在i组中对这8行扫描
            {
                ucData = ucData >> 1;
                if((Table + (usRowTmp + k) * usColumnNum)[j] == 0) ;
                 else
                    ucData = ucData | 0x80;
            }
            OLED_WrDat(ucData);
        }
    }
    OLED_Set_Pos(0,(uint8)i);                   //设置剩下的行显示的起始坐标
    usRowTmp = i << 3;                          //计算当前所在行的下标
    for(j = 0; j < usColumnNum; j++)            //列扫描
    {
        ucData = 0;
        for(k = 0; k < n; k++)                  //对剩下的行扫描
        {
            ucData = ucData >> 1;
            if((Table + (usRowTmp + k) * usColumnNum)[j] == LED_IMAGE_WHITE)
            {
                ucData = ucData | 0x80;
            }

        }
        ucData = ucData >> (8 - n);
        OLED_WrDat(ucData);
    }
}

